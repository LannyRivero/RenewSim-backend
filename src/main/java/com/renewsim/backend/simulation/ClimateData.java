package com.renewsim.backend.simulation;

public class ClimateData {
    private double irradiancia;    
    private double viento;

    public double getViento() {
        return viento;
    }

    public void setViento(double viento) {
        this.viento = viento;
    }
    private double hidrologia;

    public double getHidrologia() {
        return hidrologia;
    }

    public void setHidrologia(double hidrologia) {
        this.hidrologia = hidrologia;
    }

    public double getIrradiancia() {
        return irradiancia;
    }

    public void setIrradiancia(double irradiancia) {
        this.irradiancia = irradiancia;
    }
}

